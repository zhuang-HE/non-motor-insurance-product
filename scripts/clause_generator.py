def generate_clause_content(product_name: str, category: str, company_name: str = "太平科技保险股份有限公司") -> str:
    """生成条款内容框架"""
    
    if category not in CLAUSE_TEMPLATES:
        category = '其他'
    
    template = CLAUSE_TEMPLATES[category]
    
    clause_num = 1
    
    content = f"""---
source_file: {company_name}_{product_name}条款.docx
product: {product_name}
category: {category}
type: clause
version: 1.0
date: {datetime.now().strftime('%Y-%m-%d')}
---

# {company_name}
# {product_name}条款

## 总则
第{clause_num}条 本保险合同由保险条款、投保单、保险单、保险凭证以及批单组成。凡涉及本保险合同的约定，均应采用书面形式。
"""
    clause_num += 1
    
    content += f"""
第{clause_num}条 {template['def_content']}
"""
    clause_num += 1
    
    content += f"""
## 保险标的
第{clause_num}条 {template['coverage']}
"""
    clause_num += 1
    
    content += f"""
## 保险责任
第{clause_num}条 在保险期间内，因下列原因造成保险标的的损失或被保险人依法应承担的赔偿责任，保险人按照本保险合同的约定负责赔偿：
{template['risks']}
"""
    clause_num += 1
    
    content += f"""
## 责任免除
第{clause_num}条 下列原因造成的损失、费用，保险人不负责赔偿：
{template['exclusions_general']}
"""
    clause_num += 1
    
    content += f"""
第{clause_num}条 下列损失、费用，保险人也不负责赔偿：
{template['exclusions_specific']}
"""
    clause_num += 1
    
    content += f"""
## 保险金额/赔偿限额
第{clause_num}条 {template['amount_rule']}
"""
    clause_num += 1
    
    content += f"""
## 免赔额（率）
第{clause_num}条 {template['deductible_rule']}
"""
    clause_num += 1
    
    content += f"""
## 保险期间
第{clause_num}条 除另有约定外，保险期间为{template['period']}，以本保险合同载明的起讫时间为准。
"""
    clause_num += 1
    
    content += f"""
## 投保人/被保险人义务
第{clause_num}条 订立本保险合同时，保险人就保险标的或者被保险人的有关情况提出询问的，投保人应当如实告知，并如实填写投保单。
"""
    clause_num += 1
    
    content += f"""
第{clause_num}条 投保人应当按照本保险合同的约定交付保险费。
"""
    clause_num += 1
    
    content += f"""
第{clause_num}条 保险事故发生后，被保险人应当：
（一）尽力采取必要措施，防止或减少损失；
（二）及时通知保险人，并在知道或应当知道保险事故发生后48小时内向保险人书面说明事故发生的原因、经过和损失情况；
（三）保护事故现场，允许并配合保险人进行事故调查。
"""
    clause_num += 1
    
    content += f"""
## 赔偿处理
第{clause_num}条 保险人收到赔偿请求后，应当及时作出核定；对属于保险责任的，在与被保险人达成赔偿协议后十日内，履行赔偿义务。
"""
    clause_num += 1
    
    content += f"""
第{clause_num}条 保险人自收到赔偿请求之日起六十日内，对赔偿金额不能确定的，应当根据已有证明和资料将可以确定的数额先予以支付。
"""
    clause_num += 1
    
    content += f"""
## 代位追偿
第{clause_num}条 保险人向第三者行使代位请求赔偿权利时，被保险人应当向保险人提供必要的文件和其所知道的有关情况。
"""
    clause_num += 1
    
    content += f"""
## 争议处理
第{clause_num}条 因履行本保险合同发生的争议，由当事人协商解决；协商不成的，提交保险合同载明的仲裁机构仲裁或依法向人民法院提起诉讼。
"""
    
    return content


def main():
    """测试生成功能"""
    import sys
    
    if len(sys.argv) < 3:
        print("用法: python clause_generator.py <产品名称> <险种大类>")
        print("\n险种大类选项:")
        for cat in CLAUSE_TEMPLATES.keys():
            print(f"  - {cat}")
        return
    
    product_name = sys.argv[1]
    category = sys.argv[2]
    
    content = generate_clause_content(product_name, category)
    print(content)


if __name__ == '__main__':
    from datetime import datetime
    main()
